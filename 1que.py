 #Write a program to demonstrate different numeric data types (int, float) and type conversion.

# Integer type
a = 10
print("Value of a:", a)
print("Type of a:", type(a))

# Float type
b = 5.5
print("\nValue of b:", b)
print("Type of b:", type(b))

# Type Conversion

# Convert int to float
c = float(a)
print("\nAfter converting int to float:")
print("Value of c=", c)
print("Type of c:", type(c))

# Convert float to int
d = int(b)
print("\nAfter converting float to int:")
print("Value of d=", d)
print("Type of d:", type(d))